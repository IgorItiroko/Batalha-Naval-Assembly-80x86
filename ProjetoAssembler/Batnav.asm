TIROBASE PROC   
    TIROTEIO:
            LIMPA_TELA  ;Faz o setup da interface para o jogador dar os tiros, inteiramente por macros comentados no txt.
            MOVE_CURSOR 0,0
            MSGX MSG5   ;Imprime a msg5
            MSGX ETIRO      ;Imprime 'Tiro:'
            MSGX TIROS_RESTANTES    ;Imprime quantos tiros restam
            IM2NUM NTR      ;Decrementa o contador de tiros
            MSGX ACERTOS    ;Exibe o quanto o jogador acertou ate agora
            IMNUM COUNT1
            PULA_LINHA      
            MSGX MSG_TIRO_ANTERIOR  ;Printa qual foi o tiro anterior do jogador 1
            IMCARAC TIRO_ANTERIOR1[0]   ;Impressao do numero
            IMNUM TIRO_ANTERIOR1[1]
            JMP PULASUP     ;Serie de labels de suporte, pois os jumps estao grandes demais
SUP2:       JMP TIROTEIO    ;Segue para o tiroteio
PULASUP:    
            PULA_LINHA
            MSGX MSG8       ;Exibe a msg8
            PULA_LINHA  
            CALL IMPRIME3   ;Imprime a matriz alocada 3
            PULA_LINHA
            MSGX MSG9       ;Exibe a mssg9
            PULA_LINHA
            CALL IMPRIME5   ;Imprime a matriz alocada 5
            MOVE_CURSOR 25,0    ;Move o cursor para a posicao inicial do tiro
            CALL JOGADOR1   ;Pede para o jogador 1 comecar sua jogada
            MSGX MSG4   ;Exibe msg4
            MOV AH,1    ;Aguarda para mudar de turno
            INT 21h
            LIMPA_TELA
            MOVE_CURSOR 0,0
            MSGX MSG6   ;Exibe msg6
            MSGX ETIRO  ;Exibe tiro:
            MSGX TIROS_RESTANTES    ;Exibe quantos tiros restam
            JMP PULASUP2
SUP3:       JMP SUP2
PULASUP2:
            IM2NUM NTR  ;Exibe o contador de tiros restantes
            MSGX ACERTOS    ;Exibe quantos foram os acertos
            IMNUM COUNT2    ;Exibe o contador de acertos
            PULA_LINHA
            MSGX MSG_TIRO_ANTERIOR  ;Exibe qual foi o tiro anterior
            IMCARAC TIRO_ANTERIOR2[0]
            IMNUM TIRO_ANTERIOR2[1]
            PULA_LINHA
            MSGX MSG8   ;Exibe msg8
            PULA_LINHA
            CALL IMPRIME4   ;Chama impressao da quarta matriz
            PULA_LINHA
            MSGX MSG9   ;Exibe msg9
            PULA_LINHA
            CALL IMPRIME6   ;Imrpime a matriz 6
            MOVE_CURSOR 25,0    ;Move o cursor para chamar o jogador2
            JMP PULASUP4
SUP4:       JMP SUP3
PULASUP4:
            CALL JOGADOR2   ;Procedimento para que o jogador 2 jogue.
            MSGX MSG4       ;Exibir msg4
            MOV AH,1        
            INT 21h         ;Aguardo
            CMP COUNT1,5    ;Se o contador de acertos do jogador 1 atingiu 5, ele ganhou
            JE JOGADOR1GANHOU
            CMP COUNT2,5    ;Se o contador de acertos do jogador 2 atingiu 5, ele ganhou
            JE JOGADOR2GANHOU
            DEC NTR
            JNZ SUP4 ;LOOP COMECA AQUI
EMPATE1:     
             CALL PRINTEMPATE   ;Mostra a interface de empate
             MOV AH,1   ;Aguardo
             INT 21h
             JMP FIMDOJOGO  
JOGADOR1GANHOU:
            CMP COUNT2,5    ;Caso ambos os jogadores tenham 5 acertos mostra empate tambem
            JE EMPATE1
            CALL VITORIAJ1  ;Chama a tela de vitoria do jogador 1
            MOV AH,1    ;Aguarda
            INT 21h
            JMP FIMDOJOGO
JOGADOR2GANHOU:
            CMP COUNT1,5    ;Caso ambos os jogadores tenham obtido 5 acertos mostra empate
            JE EMPATE1
            CALL VITORIAJ2  ;Chama a tela de vitoria do jogador 2
            MOV AH,1
            INT 21h            
            
FIMDOJOGO:
    LIMPA_TELA            
    RET
TIROBASE ENDP

JOGADOR1 PROC   ;Procedimento para o jogador 1 

MSGX DAR_TIRO   ;Mostra a mensagem de tiro
CONF:
    MOV AH,1    ;Coleta o tiro do jogador e armazena o primeiro numero na variavel tiro anterior
    INT 21h
    MOV TIRO_ANTERIOR1[0],AL
    CMP AL,65       ;Faz a verificacao do caractere para ver se ele se encaixa na matriz
    JL TERRO
    CMP AL,70
    JG TERRO
    JMP SEGM
    TERRO:      ;Caso nao se encaixe imprime erro
    PULA_LINHA
    MSGX MSG3
    PULA_LINHA
    JMP CONF    ;Volta para a confirmacao
SEGM:
    AND AL,0Fh ;Transforma o caractere em numero
    DEC AL  ;Decrementa ele para que se encaixe na matriz 
    MOV BL,6 ;Multiplica BL por 6 para ele se encaixar nas colunas 
    MUL BL 
    MOV POSICAOY2,AL    ;Manda a coordenada para uma variavel.
        
    MOV AH,1 
    INT 21h 
    SUB AL,48   ;Faz a verificacao do caractere para ver se ele se encaixa na matriz
    MOV TIRO_ANTERIOR1[1],AL    ;manda o valor do segundo digito do tiro para tiro anterior 
    DEC AL  ;Decrementa AL
