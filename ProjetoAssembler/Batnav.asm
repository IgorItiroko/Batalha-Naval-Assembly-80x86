PRINTABERTURA PROC  ;Procedimento para printar a abertura

XOR SI,SI
XOR BX,BX
MOV AUX,0   ;Inicializa onde a abertura vai comecar
MOV AUY,0
MOV CONTROL,0

MOV COUNTER, 4800   ;Seta um contador para a abertura, no caso de 80x60 = 4800 pixels
REPETIDOR:  

    MOV AX, ABERTURA[BX]    ;Move o conteudo de abertura[bx] para AX
    
    CMP CONTROL,80  ;Seta o contador de colunas
    JNE LOOPCOLORE  ;Caso ele nao seja 0, pula para o procedimento de colorir os bits
    MOV CONTROL,0
    MOV AUX,0
    INC AUY
    
    LOOPCOLORE:     ;Procedimento de colorir os bits, pega o valor de AX encontrado em data e compara com um numero, cada numero corresponde a uma cor, e printa o pixel dessa cor.
    PUSH BX

    CMP AX,00
    JE BLACK
    
    CMP AX,01
    JE LIGHTBLUE

    CMP AX,07
    JE RED

    CMP AX,08
    JE BLUE

    CMP AX,04
    JE BROWN

    CMP AX,05
    JE YELLOW

    CMP AX,03
    JE WHITE

    CMP AX,06
    JE GREIBRO

    JMP FIMCORES      
    BLACK:          ;Para cada uma das cores seta um valor diferente para a variavel cor
    MOV COR,0
    CALL PRINTA
    JMP FIMCORES

LIGHTBLUE:
    MOV COR,9
    CALL PRINTA
    JMP FIMCORES

RED:
    MOV COR,4
    CALL PRINTA
JMP FIMCORES

SUPORTECOR:
JMP REPETIDOR   ;JMP auxiliar

BLUE:
    MOV COR,1
    CALL PRINTA
    JMP FIMCORES

BROWN:
    MOV COR,6
    CALL PRINTA
    JMP FIMCORES

YELLOW:
    MOV COR,14
    CALL PRINTA
    JMP FIMCORES

WHITE:
    MOV COR,15
    CALL PRINTA
    JMP FIMCORES

GREIBRO:
    MOV COR,8
    CALL PRINTA
    JMP FIMCORES

      
FIMCORES:

    POP BX

    INC AUX ;Incrementa a posicao de onde o pixel vai ser printado.

    ADD BX,2    ;Aponta para o proximo endereco de BX

    INC CONTROL ;Incrementa a coluna
    DEC COUNTER ;Decrementa o contador
    CMP COUNTER,0   ;Se o contador for 0 ele finaliza
    JNZ SUPORTECOR

    RET

    PRINTABERTURA ENDP

    PRINTA PROC     ;Procedimento para imprimir o pixel colorido.
        PUSH AX
        PUSH BX
        PUSH CX
        PUSH DX

        XOR SI,SI
        XOR BX,BX
        MOV XCOUNTER,0  ;Seta 0 para XCOUNTER
        MOV SVGACOUNT, 100  ;Faz um contador para pintar 100 pixels, normalmente ele e 1x1 mas como estamos multiplicando por 10 ele fica 10x10
    
        MOV AX,AUX  ;Move o conteudo de AUX para ser multiplicado por 10, pois a imagem esta em 80x60 e a tela em 800x600
        MUL SEPQA
        MOV X, AX

        MOV AX,AUY  ;Move o conteudo de AUX para ser multiplicaod por 10, pois a imagem esta em 80x60 e a tela em 800x600
        MUL SEPQA
        MOV Y, AX

PROCPINTA:
            CMP XCOUNTER,10 ;Compara o XCOUNTER com 10
            JNE PROCPINTA1  ;Se nao for =10 vai para o procpinta1

            MOV XCOUNTER,0  ;Se for =0 ele reinscia o contador 
            MOV AX,AUX
            MUL SEPQA
            MOV X, AX


            INC Y

PROCPINTA1:
              MOV CX, X     ;Faz a funcao 0CH do int 10h para imprimir o caractere colorido
              MOV DX, Y  
              MOV AL, COR  
              MOV AH, 0ch 
              INT 10h

              INC X   ;Incrementa a coluna 

              ADD BX,2    ;Vai para o proxima cor em ABERTURA[BX]

        INC XCOUNTER    ;Incrementa o contador
        DEC SVGACOUNT   ;Decrementa o contador de pixels do 10x10
        CMP SVGACOUNT,0 ;Compara esse contador, se nao for 0 ele retorna o processo
        JNZ PROCPINTA

        POP DX
        POP CX
        POP BX
        POP AX

RET
PRINTA ENDP

