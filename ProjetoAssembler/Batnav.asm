;PROCEDIMENTO DE COLETA DE LETRAS DO PRIMEIRO JOGADOR



 LETRAT PROC
   PUSH CX
CONFIRM:
    CALL EXIBIR1 ;Exibe a interface
    MOV AH,1    ;Aguarda
    INT 21h
    CMP AL,65   ;Pega um caractere e filtra para que ele seja dentro do alcance de atuacao da matriz
    JL ERRO     
    CMP AL,70
    JG ERRO
    JMP SEGUE
REPERRO:
    DEC NNAVIOS1    ;Decrementa o numero do navio
    MSGX MSG3       ;Exibe msg3
    JMP CONFIRM 
    
ERRO:

    PULA_LINHA      
    MSGX MSG3   ;Exibe msg3
    PULA_LINHA  
    XOR AX,AX   ;Limpa AX
    JMP CONFIRM     

SEGUE:  
    AND AL,0Fh  ;Converte para numero
    DEC AL      ;Decrementa para encaixar na matriz
    MOV BL,6    ;Multiplica por 6 para converter para um contador de linhas do 6x6
    MUL BL              
    MOV POSICAOY,AL ;Joga a posicao do tiro para o data
    
    MOV AH,1    ;Coleta um caractere, verifica se o caractere esta entre 1 e 6 para verificar se ele se encaixa nos parametross da matriz
    INT 21h
    SUB AL,49
    CMP AL,0  
    JL ERRO     ;Caso esteja fora, exibe um erro
    CMP AL,5 
    JG ERRO 
    JMP PULAREPERRO
    
SUPERRO: JMP REPERRO

PULAREPERRO:
    INC NNAVIOS1      ;BH e a funcao que conta o numero de navios selecionados
    AND AX,0Fh
    MOV BL,POSICAOY
    MOV SI,AX
    
    CMP MAT1[SI][BX+1],7Eh      ;Verifica se tem algum navio alocado nos arredores do navio posicionado.
    JE SUPERRO
    CMP MAT1[SI][BX-1],7Eh
    JE SUPERRO
    CMP MAT1[SI-6][BX],7Eh
    JE SUPERRO
    CMP MAT1[SI+6][BX],7Eh
    JE SUPERRO
    CMP MAT1[SI][BX],7Eh ;Compara a posicao selecionada com o numero 1, caso a posicao ja esteja alocada com 1 significa que esse tiro ja foi dado, logo ele exibira erro.
    JE SUPERRO
 
    MOV MAT1[SI][BX],7Eh    ;Preenche a matriz com a posicao escolhida
    MOV MAT3[SI][BX],7Eh    ;Preenche a matriz que vai ser exibida posteriormente

     
           POP CX 
RET
LETRAT ENDP


