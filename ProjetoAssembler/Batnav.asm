IMPRIME1 PROC   ;Procedimento para imprimir a matriz 1
    PUSH CX
    MOV SAGAIS,25   ;SAGAIS usado como contador
    MOV letra,65    ;Letra setado para mostrar as letras a partir do 65, ou seja , A
    XOR DI,DI    ;Inicializa DI
PRINTA_VET:   
    MOV AH,2
    MOV DL,vet1[DI] ;Printa em um processo repetitivo o vet1
    int 21h
    INC DI
    DEC SAGAIS
    CMP SAGAIS,0    
    JNE PRINTA_VET
    XOR SI,SI
    MOV CH,6
    PULA_LINHA
    LA:         ;Realiza o procedimento padrao de impressao de uma matriz 6x6, imprimindo letras para corresponder as linhas
        MOV AH,2
        MOV CL,6
        XOR BX,BX
        MOV DL,letra
        int 21h
        MOV DL,32
        int 21h
        LA2:
            MOV DL,7Ch
            int 21h
            mov dl,20h
            int 21h
            MOV DL,MAT1[SI][BX]
            ADD DL,30h
            INT 21h
            MOV DL,20h
            INT 21h
            INC BX
            DEC CL
            JNZ LA2
            PULA_LINHA
        inc letra
        ADD SI,6
        DEC CH
        JNZ LA
        
        MOV SAGAIS,0
        MOV letra,0
        POP CX
        RET
IMPRIME1 ENDP

